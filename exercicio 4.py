a = input ('coloque qualquer coisa :')

print('Qual a clase primitiva? ',type(a))
print('tem somente numero ? ',a.isnumeric())
print('tem somente letra ? ',a.isalpha())
print('tem  letras e numeros ? ',a.isalnum())
print('tem numeros decimais ? ',a.isdecimal())
print(' Está em maiusculas ? ',a.isupper())
print('Está em minusculas ? ',a.islower())
print('Está capitalizada ? ',a.istitle())



